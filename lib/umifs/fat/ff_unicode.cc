/// Walk a compressed uppercase table and apply the conversion
uint32_t apply_upper_table(uint16_t ch, const uint16_t* table) {
    const uint16_t* p = table;
    for (;;) {
        uint16_t base = *p++;
        if (base == 0 || ch < base) {
            break;
        }
        uint16_t nc_cmd = *p++;
        uint16_t cmd = nc_cmd >> 8;
        uint16_t count = nc_cmd & 0xFF;
        if (ch < base + count) {
            switch (cmd) {
            case 0:
                return p[ch - base];
            case 1:
                return ch - ((ch - base) & 1);
            case 2:
                return ch - 16;
            case 3:
                return ch - 32;
            case 4:
                return ch - 48;
            case 5:
                return ch - 26;
            case 6:
                return ch + 8;
            case 7:
                return ch - 80;
            case 8:
                return ch - 0x1C60;
            default:
                break;
            }
            break;
        }
        if (cmd == 0) {
            p += count;
        }
    }
    return ch;
}


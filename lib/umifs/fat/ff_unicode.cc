uint16_t ff_oem2uni(uint16_t oem, [[maybe_unused]] uint16_t cp) {
    if (oem < 0x80) {
        return oem;
    }
    uint16_t idx = oem - 0x80;
    if (idx < 128) {
        return cp437_to_unicode[idx];
    }
    return 0;
}

uint16_t ff_uni2oem(uint32_t uni, [[maybe_unused]] uint16_t cp) {
    if (uni < 0x80) {
        return static_cast<uint16_t>(uni);
    }
    if (uni >= 0x10000) {
        return 0;
    }
    for (uint32_t i = 0; i < 128; i++) {
        if (uni == cp437_to_unicode[i]) {
            return static_cast<uint16_t>(i + 0x80);
        }
    }
    return 0;
}

uint32_t ff_wtoupper(uint32_t uni) {
    if (uni >= 0x10000) {
        return uni;
    }
    auto ch = static_cast<uint16_t>(uni);
    const uint16_t* table = (ch < 0x1000) ? upper_cvt_0000 : upper_cvt_1000;
    return apply_upper_table(ch, table);
}

